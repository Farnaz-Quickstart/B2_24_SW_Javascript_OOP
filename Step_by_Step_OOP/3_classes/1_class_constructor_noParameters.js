// Classes are a template for creating objects. 
// They encapsulate data with code to work on that data.

// Definition of Class
class Car {
  constructor() {
    this.model = "model",
    this.color = "color"
    console.log ("This is a constructor")
  }
}

let object1 = new Car()
console.log (object1.model)
console.log (object1.color)
